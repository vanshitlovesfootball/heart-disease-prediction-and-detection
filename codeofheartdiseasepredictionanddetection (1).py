    t = np.linspace(0, 1, timesteps)

    if i < samples//2:
        # Normal ECG-like waveform
        signal = np.sin(2*np.pi*5*t) + 0.1*np.random.randn(timesteps)
        label = 0
    else:
        # Abnormal waveform
        signal = np.sin(2*np.pi*5*t) + 0.5*np.sin(2*np.pi*20*t) + 0.2*np.random.randn(timesteps)
        label = 1

