import shutil, glob

def copy_images(src_pattern, dst_folder, limit=300, prefix="real"):
    files = glob.glob(src_pattern, recursive=True)
    count = 0
    for f in files:
        if os.path.isfile(f):
            ext = os.path.splitext(f)[1].lower()
            if ext in [".png",".jpg",".jpeg",".bmp",".webp"]:
                shutil.copy(f, os.path.join(dst_folder, f"{prefix}_{count}{ext}"))
                count += 1
                if count >= limit:
                    break
    print(dst_folder, "->", count)

