def engineer_features(data):
    BMI = round(data["weight_kg"] / (data["height_m"] ** 2), 2)

    if data["age"] < 40:
        age_group = 0
    elif data["age"] < 55:
        age_group = 1
    else:
        age_group = 2

    combined_risk = (
        int(data["age"] > 55) +
        int(data["chol"] > 240) +
        int(data["trestbps"] > 140) +
        int(data["oldpeak"] > 2) +
        int(data["exang"] == 1)
    )

    data["BMI"] = BMI
    data["age_group"] = age_group
    data["combined_risk"] = combined_risk

    return data

